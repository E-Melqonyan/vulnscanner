# Sample Python code file 1

def add_numbers(a, b):
    """
    Adds two numbers.
    """
    return a + b

def multiply_numbers(a, b):
    """
    Multiplies two numbers.
    """
    return a * b

# Additional function
def subtract_numbers(a, b):
    """
    Subtracts two numbers.
    """
    return a - b

